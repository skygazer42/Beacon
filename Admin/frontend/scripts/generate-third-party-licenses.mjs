import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const frontendDir = path.dirname(path.dirname(fileURLToPath(import.meta.url)));
const lock = JSON.parse(fs.readFileSync(path.join(frontendDir, 'package-lock.json'), 'utf8'));
const outputPath = path.resolve(frontendDir, '../static/app-shell/THIRD_PARTY_LICENSES.txt');

const licenseNames = /^(licen[cs]e|copying|notice)(\..*)?$/i;

function repositoryUrl(metadata) {
  const value = metadata.repository;
  if (typeof value === 'string') return value;
  if (value && typeof value.url === 'string') return value.url;
  return metadata.homepage || '';
}

const packages = Object.entries(lock.packages || {})
  .filter(([packagePath, metadata]) => packagePath && !metadata.dev && packagePath.includes('node_modules/'))
  .map(([packagePath, lockMetadata]) => {
    const packageDir = path.join(frontendDir, packagePath);
    const metadata = JSON.parse(fs.readFileSync(path.join(packageDir, 'package.json'), 'utf8'));
    const licenseFile = fs
      .readdirSync(packageDir, { withFileTypes: true })
      .filter((entry) => entry.isFile() && licenseNames.test(entry.name))
      .map((entry) => entry.name)
      .sort()[0];

    return {
      name: metadata.name,
      version: metadata.version || lockMetadata.version,
      license: metadata.license || lockMetadata.license || 'UNKNOWN',
      repository: repositoryUrl(metadata),
      text: licenseFile
        ? fs.readFileSync(path.join(packageDir, licenseFile), 'utf8').replace(/\r\n/g, '\n').trim()
        : 'The published package did not include a standalone license file; see its repository and package metadata.',
    };
  })
  .sort((a, b) => a.name.localeCompare(b.name) || a.version.localeCompare(b.version));

const sections = packages.map(
  (item) => [
    '-'.repeat(80),
    `${item.name}@${item.version}`,
    `SPDX/license metadata: ${item.license}`,
    item.repository ? `Upstream: ${item.repository}` : '',
    '',
    item.text,
  ].filter(Boolean).join('\n'),
);

const header = [
  'Beacon frontend third-party licenses',
  '',
  'Generated from production packages in Admin/frontend/package-lock.json.',
  'Do not edit this file by hand; run `npm run build` in Admin/frontend.',
  '',
].join('\n');

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, `${header}${sections.join('\n\n')}\n`, 'utf8');
